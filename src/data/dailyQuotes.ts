/**
 * Get a quote based on the day of the year.
 * Each day of the year will return a different quote (1-365).
 */
export function getDailyQuote(): DailyQuote {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Use modulo to ensure we stay within the array bounds
  const quoteIndex = (dayOfYear - 1) % dailyQuotes.length;
  
  return dailyQuotes[quoteIndex];
}
